#include "conv.h"

double cm2inch(double cm)
{
return  cm * 0.393;
}

double inch2cm(double inch)
{
return  inch * 2.54;
}
double m2ft(double m)
{
return  m * 3.3;
}

double ft2m(double ft)
{
return  ft * 0.3;
}

double km2mil(double km)
{
return km * 0.62;
}

double mil2km(double mil)
{
return mil * 1.61;
}

